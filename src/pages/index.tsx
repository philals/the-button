import type { NextPage } from "next";
import Head from "next/head";
import { useState } from "react";
import { DefaultQueryCell } from "../utils/DefaultQueryCell";
import { trpc } from "../utils/trpc";

const Home: NextPage = () => {
  const lastClickedQuery = trpc.useQuery(["button.lastClicked"]);
  const [input, setInput] = useState("");
  const buttonClickedMutation = trpc.useMutation(["button.clicked"]);

  return (
    <>
      <Head>
        <title>The Button</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="container mx-auto flex flex-col items-center justify-center h-screen p-4">
        <label
          htmlFor="exampleFormControlInput1"
          className="form-label inline-block mb-2 text-gray-700"
        >
          Example label
        </label>
        <input
          type="text"
          className="
        form-control
        block
        w-full
        px-3
        py-1.5
        text-base
        font-normal
        text-gray-700
        bg-white bg-clip-padding
        border border-solid border-gray-300
        rounded
        transition
        ease-in-out
        m-0
        focus:text-gray-700 focus:bg-white focus:border-blue-600 focus:outline-none
      "
          id="exampleFormControlInput1"
          placeholder="Example label"
          onChange={(e) => setInput(e.target.value)}
          value={input}
        />

        <DefaultQueryCell
          query={lastClickedQuery}
          success={({ data }) => {
            return (
              <>
                <h1 className="text-5xl md:text-[5rem] leading-normal font-extrabold text-gray-700">
                  The last person to click was{" "}
                  <span className="text-purple-300">{data?.name}</span>
                  {" on "}
                  <span className="text-blue-300">
                    {data?.createdAt.toLocaleString()}
                  </span>
                </h1>
                <div className="flex space-x-2 justify-center">
                  <button
                    onClick={() =>
                      buttonClickedMutation.mutate({ name: input })
                    }
                    type="button"
                    className="inline-block px-6 py-2.5 bg-red-600 text-white font-medium text-xs leading-tight uppercase rounded shadow-md hover:bg-red-700 hover:shadow-lg focus:bg-red-700 focus:shadow-lg focus:outline-none focus:ring-0 active:bg-red-800 active:shadow-lg transition duration-150 ease-in-out"
                  >
                    Button
                  </button>
                </div>
              </>
            );
          }}
          empty={() => <p>Err.. there is meant to be data here</p>}
        />
      </main>
    </>
  );
};

export default Home;
